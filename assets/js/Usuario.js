class Usuario {
	constructor(usuarioId, nif, name, surname, username, email, password, direccion, cp, ciudad, pais, provincia, phone, admin) {
		this.usuarioId = usuarioId;
		this.nif = nif;
		this.name = name;
		this.surname = surname;
		this.username = username;
		this.email = email;
		this.password = password;
		this.direccion = direccion;
		this.cp = cp;
		this.ciudad = ciudad;
		this.pais = pais;
		this.provincia = provincia;
		this.phone = phone;
		this.admin = admin;
	}
}